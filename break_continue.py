# Break Statement
print("The loop with 'break' produces output as :")
for i in range(1, 11):
    if i % 3 == 0:
        break
    else:
        print(i)

# Continue Statement
print("The loop with 'continue' produces output as :")
for i in range(1, 11):
    if i % 3 == 0:
        continue
    else:
        print(i)